    if (user) {
      const match = await bcrypt.compare(password, user.password);
      console.log("Password match:", match);
    }

    if (user && (await bcrypt.compare(password, user.password))) {
      res.json({
        _id: user._id,
        name: user.name,
        email: user.email,
        token: generateToken(user._id),
      });
    } else {
      res.status(401).json({
        message: "Invalid Email or Password",
      });
    }
